#ifndef __DEVILS_UTILITY_H__
#define __DEVILS_UTILITY_H__

#define DEVILS_MAX(x, y) ((x) > (y) ? (x) : (y))
#define DEVILS_MIN(x, y) ((x) < (y) ? (x) : (y))
#define DEVILS_DIFFERENCE(x, y) ((x) < (y) ? (y) - (x) : (x) - (y))

#endif /* __DEVILS_UTILITY_H__ */
